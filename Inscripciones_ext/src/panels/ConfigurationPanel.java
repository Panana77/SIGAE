    private void jbAplyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAplyActionPerformed

        try 
        {
            //Confirmar que se desean guardar los cambios
            int confirmSave = JOptionPane.showConfirmDialog(null, "¬øGuardar cambios?");
            if (confirmSave == 0) 
            {
                //Verificar si se ha modificado el limite del cupo
                if (isLimitCupo) 
                {
                    //Asignar el cupo al objeto activity
                    Activity activity = new Activity(Integer.parseInt(jtfCupo.getText()));
                    //Crear el objeto activityDAO
                    ActivityDAO activityDAO = new ActivityDAO();
                    //Establecer el cupo limite en las actividades
                    activityDAO.update(activity, jcbActivities.getSelectedItem().toString());
                   
                    //desabilitar el boton de aplicar configuracion
                    jbAply.setEnabled(false);
                    //Dar a conocer que ya no se esta realizando ninguna configuracion
                    isLimitCupo = false;
                }
                //Verificar si se esta agregando una nueva actividad
                if(isAddNewActivity)
                {
                    //Crear objeto activity para insertar los datos
                    Activity activity = new Activity(jtfActivityName.getText(),jcbActivityType.getSelectedIndex() + 1,Integer.parseInt(jtfRegisterLimit.getText()));
                    
                    //Crear objecto ActivityDAO para insertar los datos en la bd
                    ActivityDAO activityDAO = new ActivityDAO();
                    activityDAO.insert(activity);
                    //Dar a conocer que ya se ha agregado una nueva actividad
                    isAddNewActivity = false;
                    
                }
                
                 //Mensaje de confirmacion
                JOptionPane.showMessageDialog(null, "¬°Configuraci√≥n guardada!");
                JOptionPane.showMessageDialog(null, "Favor de reinicie el programa para usar la nueva configuraci√≥n");
            }
            
        } 
        catch (Exception ex) 
        {        

            JOptionPane.showMessageDialog(null, "Verifique los datos");
        }
    }//GEN-LAST:event_jbAplyActionPerformed

    private void jtfCupoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jtfCupoActionPerformed
        //Verificar que el jtextField contenga texto
        if(jtfCupo != null)
        {
            //Dar a conocer que se ha realizado un cambio en el limite del cupo
            isLimitCupo = true;
            //habilitar el boton de aplicar configuracion
            jbAply.setEnabled(true);
        }
        
    }//GEN-LAST:event_jtfCupoActionPerformed

    private void jcbActivitiesItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jcbActivitiesItemStateChanged
       try
       {
           //Crear objeto de la clase ActivityDAO
           ActivityDAO activityDAO = new ActivityDAO();
           //Obtener los registros de la tabla de actividades
           List<Activity> activities = activityDAO.select();
           
           //Asignar los nombres de las actividades a jtfCupo
           for(Activity activity : activities)
           {
               if(activity.getActivityName().equals(jcbActivities.getSelectedItem().toString()))
               {
                   //asignar el limite al componente
                   jtfCupo.setText(String.valueOf(activity.getLimit()));
               }
           }
       }
       catch(Exception ex)
       {
           JOptionPane.showMessageDialog(null, ex);
       }
    }//GEN-LAST:event_jcbActivitiesItemStateChanged

